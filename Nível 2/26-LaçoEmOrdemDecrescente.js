//Escreva um algoritmo para imprimir os números de 1 (inclusive) a 10 (inclusive) em ordem decrescente. 


//Método for
let valor = 10

for (let valor = 10; valor > 0; valor--){
    console.log(valor)
}

//Método while
valor = 10
while(valor > 0){
    console.log(valor)
    valor--
}

//Método While Do
valor = 10

do {
    console.log(valor)
    valor--
} while(valor > 0)