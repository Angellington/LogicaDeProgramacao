//A Definição de "let" e "const"


//let - Variável mutável, ela pode ser mudada

let cor = "azul"

console.log("(fixo) A cor atualmente é ", cor)
cor = "vermelho"
console.log("(fixo) A cor agora é ", cor)


//const - A variável é constante, não pode alterar

const nome = "Isabela Alves Aloe Vera"

console.log("(constante) O nome dela é ", nome)

